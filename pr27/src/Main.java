//
//private interface Interface {
//  ...
//}
//Область видимости:
//        •	Интерфейс с модификатором private также является недопустимым в Java.
//Интерфейсы не могут быть приватными, потому что они должны быть доступны другим классам,
//которые могут их реализовывать.
//Такой интерфейс может быть объявлен только в теле класса или в другом интерфейсе,
//но не как самостоятельный интерфейс с модификатором private. Код с таким объявлением вызовет ошибку компиляции.