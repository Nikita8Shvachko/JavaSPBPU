// Класс для тестирования различных способов вызова статического метода
public class Main {
    public static void main(String[] args) {
        // Вариант 1: Вызов статического метода через имя класса
        A.printVars();

        // Вариант 2: Вызов статического метода через объект класса (не рекомендуется)
        A obj = new A();
        obj.printVars(); // Работает, но вызывает предупреждение компилятора

        // Вариант 3: Вызов статического метода через класс-потомок (при наследовании)
        B.printVars();

        // Вариант 4: Вызов статического метода внутри другого статического метода
        displayStatic();
    }

    // Вспомогательный статический метод для демонстрации вызова внутри другого статического метода
    public static void displayStatic() {
        A.printVars();
    }
}

// Класс-наследник для демонстрации вызова статических методов через наследование
class B extends A {
    // Наследует статический метод printVars() от класса A
}